package br.lanxcables.com.automation.models;

public class Supplier {
    private String name;
    private int cnpj;

    public Supplier(String name, int cnpj) {
        this.name = name;
        this.cnpj = cnpj;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCnpj() {
        return cnpj;
    }

    public void setCnpj(int cnpj) {
        this.cnpj = cnpj;
    }

}
